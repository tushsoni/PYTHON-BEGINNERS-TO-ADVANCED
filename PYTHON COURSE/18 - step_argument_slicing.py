# ***********************************************               STEP ARGUMENT                ******************************************** #

# STEP ARGUMENT IS USED TO JUMP FROM ONE CHARACTER TO ANOTHER AS OUR NEED

#SYNTAX ---------->>>>>>       [STOP ARGUMENT : STOP ARGUMENT+1 : STEP ARGUMENT]

print("TUSHAR"[0:5])                   # it will simply print TUSHA

print("TUSHAR"[0:5:1])                 # it will simply print TUSHA

print("TUSHAR"[0:5:2])                 # IT WILL JUMP TO ONE CHARACTER , AND PRINT TSA

print("TUSHAR"[0:6:3])                 # IT WILL JUMP TO TWO CHARACTER AND PRINT TH

print("PYTHONISAGREATLANGUAGE"[0:22:4])   # IT WILL JUMP TO THREE CHARACTERS AND  PRINT POAANG

print("PYTHONISAGREATLANGUAGE"[0::4])       # IT WILL JUMP TO THREE CHARACTERS AND  PRINT POAANG

print("TUSHAR"[5::-1])                      # IT WILL REVERSE THE STRING an print RAHSUT

print("TUSHAR"[-1::-1])                      # IT WILL REVERSE THE STRING an print RAHSUT
 
print("TUSHAR"[::-1])                       # IT WILL REVERSE THE STRING an print RAHSUT