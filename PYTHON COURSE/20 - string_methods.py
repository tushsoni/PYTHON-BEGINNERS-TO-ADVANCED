# ***********************                          STRING METHODS - PART 1                   ************************************** #

name = "TusHaR sOnI"

# 1)    len() function                ----------------->>>>> it will count the length of characters(it also includes the space)

length = len(name)
print(length)

           # or

print(len(name))


# 2) lower()  method                 ----------------------->>>>>>> it will lower(small) all the characters in the string
 
print(name.lower())


# 3) upper()  method                 --------------------------->>>>>> it will capital all the letters in the string

print(name.upper())


# 4) title()  method                 -------------------------->>>>>>>> it will capital first letter of the word

print(name.title())


# 5) count() method                  --------------------------->>>>>>>>>>  it will count the particular letter that how many times it is occuring in the string

print(name.count("s"))