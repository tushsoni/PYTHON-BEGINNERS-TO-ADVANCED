# *****************************************         SLICING / SELECTING SUB SEQUENCES       ************************************* #

#  SLICING IS USED TO PRINT ONE OR MORE CHARACTER

# SYNTAX ---------->>>>>>>   [START ARGUMENT : STOP ARGUMENT+1]

lang = "python"

# Lets take an example , If we have to print our string from t to o then we will write :

print(lang[2:5])                     #it will print tho

print(lang[-3:6])                    #it will print hon

print(lang[:])                       # full string will be printed ie. python

print(lang[1:])                      # it will print ython

print(lang[:5])                       # it will print pytho
 
