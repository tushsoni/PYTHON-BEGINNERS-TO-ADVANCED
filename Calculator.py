#******************************** PRECEDENCE RULE ***************************************#


#     OPERATORS                       -                PRECEDENCE AND ASSOCIATIVITY RULE

# 1 -> PARANTHESIS                     -                HIGHEST
# 2 -> EXPONENT                        -                RIGHT TO LEFT
# 3 -> * , / , // , %                  -                LEFT TO RIGHT
# 4 -> + , -                           -                LEFT TO RIGHT

print((2+3)/2)                              # 2+3 =5 then 5/2 = 2.5

print((2+3)*5/2%6)                          # 2+3 =5 then 5*5 =25 then 25/2 =12.5 then 12.5%6 =0.5
                                               
